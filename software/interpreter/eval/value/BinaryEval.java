package eval.value;

import eval.funcs.BinaryBoolBoolToBool;
import eval.funcs.BinaryIntIntToBool;
import eval.funcs.BinaryIntIntToInt;

public class BinaryEval
{
   private String _found;
   protected BinaryEval(String ltype, String rtype)
   {
      _found = ltype + " * " + rtype;
   }
   public Value eval()
      throws BinaryEvalException
   {
      throw new BinaryEvalException(_found);
   }
}
